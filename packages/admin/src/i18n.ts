export const dictionaries = Object.fromEntries(
  supportedLocales.map((locale) => [locale, mergeMessages(en, overrides[locale as Exclude<Locale, "en">])]),
) as Record<Locale, Messages>;

function mergeMessages(base: Messages, override: MessageOverrides | undefined): Messages {
  if (!override) return base;
  return {
    ...base,
    ...override,
    tabs: { ...base.tabs, ...override.tabs },
    status: { ...base.status, ...override.status } as Record<string, string>,
    priority: { ...base.priority, ...override.priority },
    metrics: { ...base.metrics, ...override.metrics },
    overview: { ...base.overview, ...override.overview },
    plan: { ...base.plan, ...override.plan },
    wiki: { ...base.wiki, ...override.wiki },
    integrations: { ...base.integrations, ...override.integrations },
    plugins: { ...base.plugins, ...override.plugins },
    language: { ...base.language, ...override.language },
    pwa: { ...base.pwa, ...override.pwa },
  };
}

export function detectInitialLocale(): Locale {
  const stored = globalThis.localStorage?.getItem("projectflare.locale");
  if (isLocale(stored)) return stored;

  const language = globalThis.navigator?.language || "en";
  return matchLocale(language);
}

function matchLocale(language: string): Locale {
  const normalized = language.toLowerCase();
  const exact = supportedLocales.find((locale) => locale.toLowerCase() === normalized);
  if (exact) return exact;
  if (normalized.startsWith("es-")) return normalized === "es-es" ? "es-ES" : "es-419";
  if (normalized.startsWith("pt-")) return "pt-BR";
  const base = supportedLocales.find((locale) => locale.toLowerCase().split("-")[0] === normalized.split("-")[0]);
  return base ?? "en";
}

function isLocale(value: string | null | undefined): value is Locale {
  return supportedLocales.includes(value as Locale);
}
